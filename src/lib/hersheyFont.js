/**
 * Convert a text string to an array of SVG path `d` strings + their lengths.
 * Each path is one pen-stroke segment (natural drawing order).
 * @param {string} text
 * @param {number} size  approximate cap-height in px
 * @returns {{ paths: string[], width: number, height: number }}
 */
export function textToPaths(text, size = 64) {
  const scale = size / 21 // Hershey chars span ~21 units tall (-12 to 9)
  const paths = []
  let cx = 0

  for (const ch of text) {
    const data = C[ch]
    if (!data) { cx += 8 * scale; continue }
    for (const seg of data.s) {
      if (seg.length < 2) continue
      let d = ''
      for (let i = 0; i < seg.length; i++) {
        const px = cx + seg[i][0] * scale
        const py = (seg[i][1] + 12) * scale
        d += `${i === 0 ? 'M' : 'L'}${px.toFixed(1)},${py.toFixed(1)} `
      }
      paths.push(d.trim())
    }
    cx += data.r * scale
  }

  return { paths, width: cx, height: size }
}
